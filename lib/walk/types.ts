/**
 * [2026-07-22 신규 — 책 산책(핸드오프 v3 21장)] 스탬프에 들어가는 산책 데이터.
 *
 * 도착 인증 스탬프는 검색 맥락에서 이미 아는 값(책 제목/저자/표지·도서관명)을
 * 자동 프리필한다(21-5). 거리·걸음수는 출발 스탬프 위치→도서관 카카오 계산
 * (온디맨드)으로 채우며, 없으면 스탬프에서 생략한다.
 */
export type WalkStampData = {
  /** 책 제목 (검색 맥락에서 프리필) */
  bookTitle: string;
  /** 저자 (선택) */
  bookAuthor?: string;
  /** 표지 이미지 URL (선택) — 방식3(화면캡처)에서 <img>로 표시.
   *  방식1(사진 합성)은 이번 단계에서 표지 이미지를 canvas에 얹지 않음
   *  (외부 CDN CORS 회피 — 표지 합성은 방식2에서). */
  coverUrl?: string;
  /** 도착 목적지 도서관명 (마커의 도서관) */
  libraryName: string;
  /** 도착(인증) 시각 — 기기 시계 */
  arrivedAt: Date;
  /** 출발→도서관 이동 거리(km). 카카오 계산 성공 시에만. */
  distanceKm?: number;
  /** 환산 추정 걸음수(거리÷0.75). 실제 만보계 아님 — "약 N보"로 표기. */
  steps?: number;
  /** 누적 "N번째 산책"(localStorage). 없으면 생략. */
  walkCount?: number;
};
